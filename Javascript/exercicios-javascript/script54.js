function Ex54(){

    alert("54.  Crie um programa que exiba os números pares de 1 a 50 usando o loop while.")

 

    let numero=1

 

    while(numero<=50){

        if(numero%2==0){ //numero mod 2 = 0

            alert(numero +" é par.")

        }else{

            alert(numero +" é impar.")

        }

        numero++

    }

}    